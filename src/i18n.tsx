const jsonOverrides: Partial<Record<Lang, Dict>> = { en: enOverrides as Dict, es: esOverrides as Dict, fr: frOverrides as Dict };

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

function detectLang(): Lang {
  if (typeof navigator !== 'undefined') {
    const n = navigator.language.toLowerCase();
    if (n.startsWith('es')) return 'es';
    if (n.startsWith('fr')) return 'fr';
    if (n.startsWith('ar')) return 'ar';
    if (n.startsWith('en')) return 'en';
  }
  return 'es';
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window === 'undefined') return 'es';
    // Derivar idioma desde la ruta hash si existe: "#/fr/", "#/es/", "#/en/"
    const match = window.location.hash.match(/^#\/(en|es|fr)\b/);
    if (match) return match[1] as Lang;
    const stored = window.localStorage.getItem('lang') as Lang | null;
    return stored ?? detectLang();
  });

  const dir: 'ltr' | 'rtl' = lang === 'ar' ? 'rtl' : 'ltr';

  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = lang;
      document.documentElement.dir = dir;
    }
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('lang', lang);
    }
  }, [lang, dir]);

  const t = useMemo(() => {
    const baseDict = dicts[lang] ?? dicts.en;
    const merged = jsonOverrides[lang] ? { ...baseDict, ...jsonOverrides[lang]! } : baseDict;
    const base = (key: string) => merged[key] || dicts.en[key] || key;
    return (key: string, params?: Record<string, string | number>) => {
      const val = base(key);
      if (!params) return val;
      return val.replace(/\{\{\s*([^}]+)\s*\}\}/g, (m, p1) => {
        const k = String(p1).trim();
        return Object.prototype.hasOwnProperty.call(params, k) ? String(params[k]) : m;
      });
    };
  }, [lang]);

  const setLang = (l: Lang) => setLangState(l);

  const value: I18nContextValue = { lang, dir, t, setLang };
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}
